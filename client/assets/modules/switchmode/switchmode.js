function handleSwitchMode(message)
{
    //Thème automne
	if (message == "&#127810;")
    {
		$("body").css("background-image", "url(https://media.giphy.com/media/JoCcAa8oszgDXszKYR/giphy.gif)");
		$("body").css("background-repeat", "no-repeat");
		$("body").css("background-size", "cover");
	}

	//Thème nuit
	if (message == "&#127761;")
    {
		$("body").css("background-image", "url(https://media.giphy.com/media/FlodpfQUBSp20/giphy.gif)");
		$("body").css("background-repeat", "no-repeat");
		$("body").css("background-size", "cover");
	}

	//Thème amour
	if (message == "&#128151;")
    {
		$("body").css("background-image", "url(https://media.giphy.com/media/3o7TKoWXm3okO1kgHC/giphy.gif)");
		$("body").css("background-repeat", "no-repeat");
		$("body").css("background-size", "cover");
    }

    //Thème dollar
    if (message == "&#129297;")
    {
		$("body").css("background-image", "url(https://media.giphy.com/media/jSWPqcU7kQhoxq2Ti9/giphy.gif)");
		$("body").css("background-repeat", "no-repeat");
		$("body").css("background-size", "cover");
    }


    //Thème renard
    if (message == "&#129418;")
    {
		$("body").css("background-image", "url(https://media.giphy.com/media/xTiQypxnT56epAhIWI/giphy.gif)");
		$("body").css("background-repeat", "no-repeat");
		$("body").css("background-size", "cover");
    }

    //Thème 1+1=2
    if (message == "1+1=2")
    {
        $("body").css("background-image", "url(https://media.giphy.com/media/BmmfETghGOPrW/source.gif)");
        $("body").css("background-repeat", "no-repeat");
        $("body").css("background-size", "cover");
    }
}


